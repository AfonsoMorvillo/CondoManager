   private String getSelect() {
      Select select = new Select( "Casa c" );

      select.add( "numero", "Número" );
      select.add( "bloco", "Bloco" );
      select.add( "nome", "Proprietário" );
      select.add( "p.idProprietario", "ID" );
      select.add( "dataRegistro", "Registro" );
      select.add( "vagas", "Vagas" );
      select.addLeftJoin( "Proprietario p", "p.idProprietario = c.idProprietario" );

      if( proprietario != null ){
         select.addWhere( "c.idProprietario " + proprietario.getIdProprietario() );
      }

      if( !StringUtils.isEmpty( fieldBloco.getText().trim() ) ){
         select.addWhere( "bloco LIKE '%" + fieldBloco.getText().trim() + "%'" );
      }

      if( !StringUtils.isEmpty( fieldNumero.getText().trim() ) ){
         select.addWhere( "numero = " + fieldNumero.getText().trim() );
      }

      return select.build();
   }


