     private void fillFields() {

      fieldAno.setText(data.getAno());
      fieldCor.setText( data.getCor());
      fieldMarca.setText( data.getMarca());
      fieldModelo.setText( data.getModelo());
      fieldPlaca.setText( data.getPlacaCarro());
      fieldTipo.setText( data.getTipo());
      fieldCasa.setText(String.valueOf(data.getCasa()));
   }


   private void dataDown() {

      data.setAno(fieldAno.getText() );
     
      data.setCor(fieldCor.getText() );
      data.setMarca(fieldMarca.getText() );
      data.setModelo(fieldModelo.getText() );
      data.setPlacaCarro(fieldPlaca.getText() );
      data.setTipo( fieldTipo.getText());
      
      if (!formEdicao){
           data.setCasa(numeroCasa);
      }
     
   }

